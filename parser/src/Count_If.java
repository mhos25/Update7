public class Count_If {
    
    private int num;
    
   public Count_If( int i){
       this.num = i;
    }
   
   public void setValue(int num){
   this.num = num;
   }
   
   public int getNum(){
       return num;
   }
   
   public void increment(){
       num ++;
   }
    
}
